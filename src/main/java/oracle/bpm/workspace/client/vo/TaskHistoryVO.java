package oracle.bpm.workspace.client.vo;

public class TaskHistoryVO {
	private String updateBy;
	private String updateByDisplayName;
	private String updateDate;
	private String outcome;
	public String getUpdateBy() {
		return updateBy;
	}
	public void setUpdateBy(String updateBy) {
		this.updateBy = updateBy;
	}
	public String getUpdateByDisplayName() {
		return updateByDisplayName;
	}
	public void setUpdateByDisplayName(String updateByDisplayName) {
		this.updateByDisplayName = updateByDisplayName;
	}
	public String getUpdateDate() {
		return updateDate;
	}
	public void setUpdateDate(String updateDate) {
		this.updateDate = updateDate;
	}
	public String getOutcome() {
		return outcome;
	}
	public void setOutcome(String outcome) {
		this.outcome = outcome;
	}
	
	
}
